package com.jaenyeong._01_string_array._06_meetingroom;

public class Meeting {
    private final int start;
    private final int end;

    public Meeting(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }
}
